      (function() {
        let devtoolsOpen = false;
        setInterval(() => {
          const threshold = 160;
          if (
            window.outerHeight - window.innerHeight > threshold ||
            window.outerWidth - window.innerWidth > threshold
          ) {
            if (!devtoolsOpen) {
              devtoolsOpen = true;
              document.body.innerHTML = '';
              window.location.href = 'https://google.com';
            }
          } else {
            devtoolsOpen = false;
          }
        }, 500);
      })();
