    function formatarCPF(input) {
      var cpf = input.value.replace(/[^0-9]/g, '');
      if (cpf.length <= 11) {
        input.value = cpf.replace(/(\d{3})(\d{3})(\d{3})(\d{2})/g, '$1.$2.$3-$4');
      }
    }

    function verificarCPF(input) {
      var cpf = input.value.replace(/[^0-9]/g, '');
      var erro = document.getElementById('cpf-erro');
      if (cpf.length === 11) {
        if (!validarCPF(cpf)) {
          erro.classList.remove('hidden');
        } else {
          erro.classList.add('hidden');
          document.getElementById('form-cpf').submit(); // Enviar o formulário quando CPF for válido
        }
      } else {
        erro.classList.add('hidden');
      }
    }

    function validarCPF(cpf) {
      var soma = 0;
      var resto;
      var igual = true;

      for (var i = 1; i <= 9; i++) {
        soma += parseInt(cpf.substring(i - 1, i)) * (10 - i + 1);
        if (cpf.substring(i - 1, i) !== cpf.substring(0, 1)) {
          igual = false;
        }
      }

      resto = soma % 11;
      if (resto < 2) {
        resto = 0;
      } else {
        resto = 11 - resto;
      }

      if (resto !== parseInt(cpf.substring(9, 10)) || igual) {
        return false;
      }

      soma = 0;
      for (var i = 1; i <= 10; i++) {
        soma += parseInt(cpf.substring(i - 1, i)) * (11 - i + 1);
      }

      resto = soma % 11;
      if (resto < 2) {
        resto = 0;
      } else {
        resto = 11 - resto;
      }

      if (resto !== parseInt(cpf.substring(10, 11))) {
        return false;
      }

      return true;
    }
  </script>



